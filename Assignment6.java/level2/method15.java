public class method15 {

    
    public static double feetToYards(double feet) {
        return feet / 3.0;
    }

    public static double feetToMiles(double feet) {
        return feet / 5280.0;
    }

    public static double inchesToCentimeters(double inches) {
        return inches * 2.54;
    }

    public static double centimetersToInches(double cm) {
        return cm / 2.54;
    }

    public static double metersToKilometers(double meters) {
        return meters / 1000.0;
    }

    public static double kilometersToMeters(double km) {
        return km * 1000.0;
    }

   
    public static double kilogramsToPounds(double kg) {
        return kg * 2.20462;
    }

    public static double poundsToKilograms(double lb) {
        return lb / 2.20462;
    }


    public static double celsiusToFahrenheit(double c) {
        return (c * 9.0 / 5.0) + 32.0;
    }

    public static double fahrenheitToCelsius(double f) {
        return (f - 32.0) * 5.0 / 9.0;
    }

    
    public static double litersToMilliliters(double liters) {
        return liters * 1000.0;
    }

    public static double millilitersToLiters(double ml) {
        return ml / 1000.0;
    }
}
